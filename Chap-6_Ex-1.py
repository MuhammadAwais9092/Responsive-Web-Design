# Chapter 6 Exercise 1: Basic Math

import math

# Find the ceil of a
a_ceil = math.ceil(2.3)
print(f"Ceil of 2.3: {a_ceil}")

# Find the floor of a
a_floor = math.floor(2.3)
print(f"Floor of 2.3: {a_floor}")

# Find the factorial of a
a_factorial = math.factorial(5)
print(f"Factorial of 5: {a_factorial}")

# Find the value of 2^3
a_power = math.pow(2, 3)
print(f"Value of 2^3: {a_power}")

# Find the square root of a
a_sqrt = math.sqrt(16)
print(f"Square root of 16: {a_sqrt}")
